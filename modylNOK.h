#include <vector>
#include <string>

using namespace std;

/**
  Преобразует строку в вектор цифр.
  str - входная строка, представляющая большое число.
  Возвращает вектор, где каждый элемент соответствует одной цифре числа.
 */
vector<int> stringToVector(const string& str);

/**
  Проверяет, является ли строка корректным числом.
  str - строка для проверки.
  Возвращает true, если строка состоит только из цифр, иначе false.
 */
bool isValidNumber(const string& str);

/**
  Проверяет, представляет ли строка число, которое больше или равно 10^11.
  str - строка, представляющая число.
  Возвращает true, если число >= 10^11, иначе false.
 */
bool isAtLeast10To11(const string& str);

/**
  Умножает два больших числа, представленных векторами цифр.
  num1 - первый множитель.
  num2 - второй множитель.
  Возвращает вектор, представляющий произведение чисел.
 */
vector<int> multiply(const vector<int>& num1, const vector<int>& num2);

/**
  Делит большое число на другое большое число.
  num - делимое, представленное вектором цифр.
  divisor - делитель, представленный вектором цифр.
  Возвращает вектор, представляющий результат деления.
 */
vector<int> divide(const vector<int>& num, const vector<int>& divisor);

/**
  Вычитает одно большое число из другого.
  num1 - уменьшаемое, представленное вектором цифр.
  num2 - вычитаемое, представленное вектором цифр.
  Возвращает вектор, представляющий разность чисел.
 */
vector<int> subtract(const vector<int>& num1, const vector<int>& num2);

/**
  Сравнивает два больших числа.
  num1 - первое число, представленное вектором цифр.
  num2 - второе число, представленное вектором цифр.
  Возвращает:
    1, если num1 > num2;
    0, если num1 == num2;
   -1, если num1 < num2.
 */
int compare(const vector<int>& num1, const vector<int>& num2);

/**
  Вычисляет наибольший общий делитель (НОД) двух больших чисел.
  a - первое число, представленное вектором цифр.
  b - второе число, представленное вектором цифр.
  Возвращает вектор, представляющий НОД чисел a и b.
 */
vector<int> gcd(vector<int> a, vector<int> b);

/**
  Вычисляет наименьшее общее кратное (НОК) двух больших чисел.
  a - первое число, представленное вектором цифр.
  b - второе число, представленное вектором цифр.
  Возвращает вектор, представляющий НОК чисел a и b.
 */
vector<int> lcm(const vector<int>& a, const vector<int>& b);

/**
  Выводит большое число, представленное вектором цифр.
  num - вектор, представляющий число.
 */
void printVector(const vector<int>& num);


